Với phần lợi nhuận thu được, sẽ được đầu tư ngược trở lại để duy trì các hoạt động phục vụ cho chính cộng đồng bóng rổ Việt Nam như: tổ chức mini game, tổ chức giải đấu, tài trợ, các hoạt động quảng bá môn bóng rổ… Vâng, còn rất nhiều hoạt động sẽ chỉ là ý tưởng nằm trên giấy nếu không có nguồn kinh phí.

